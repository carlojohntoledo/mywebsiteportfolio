{
    ProgSkillsLink.addEventListener('click', function (event) {
        event.preventDefault(); // Prevent default link behavior
        showProgramming(); // Call the function to show programming details
    });

    // Get all the links inside #row-one-container
    const links = document.querySelectorAll('#skills-navbar a');

    // Add click event listeners to each link
    links.forEach(link => {
        link.addEventListener('click', function (event) {
            // Prevent the default behavior of the link
            event.preventDefault();

            // Remove the 'active' class from all links
            links.forEach(link => link.classList.remove('active'));

            // Add the 'active' class to the clicked link
            this.classList.add('active');
        });
    });
}


